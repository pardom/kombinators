// Φ combinator - phoenix

fun <A, B, C, D> Φ(): ((B) -> (C) -> D) -> ((A) -> B) -> ((A) -> C) -> (A) -> D =
    { f -> { g -> { h -> { x -> f(g(x))(h(x)) } } } }

fun <A, B, C, D> phoenix() = Φ<A, B, C, D>()
